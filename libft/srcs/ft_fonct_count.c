#include "ft_printf.h"

size_t	ft_intlen(int m)
{
	size_t i;
	size_t n;

	i = 1;
	if (m < 0)
		m = m * -1;
	n = (size_t)m;
	while (n / 10 != 0)
	{
		i++;
		n = n / 10;
	}
	return (i);
}
